# Clase que representa a los rectangulos
class Rectangulo:
    def __init__(self, w, h, color):
        # Al ancho le asigno un random entero entre 10 y 50
        self.w = w

        # A la altura le asigno un random entero entre 10 y 75
        self.h = h

        # Le asigno un color
        self.color = color